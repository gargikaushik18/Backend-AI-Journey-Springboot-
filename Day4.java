public class Day4 {

    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        for (int i = 2; i <= 20; i = i + 2 ) {
            System.out.println(i);
        }
        int j = 1;
        while (j <= 5) {
            System.out.println(j);
            j++;
        }
        int k = 1;

        do {
            System.out.println(k);
            k++;
        } while (k <= 5);

    }
}
